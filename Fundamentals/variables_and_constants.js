/*
 * -> Variables and Constants
 * They are used to store values in a program.
 * The difference is that variables can have their values changed, while constants have immutable values once defined.
 * In JavaScript, there are three main ways to declare a variable: var, let, and const.
 */

// Var: declared with function or global scope
var a = 1;

// Let: declared with block scope, being the most recommended form
let b = 2;

// Const: declared with block scope, being an immutable value
const c = 3;

console.log(a, b, c);
